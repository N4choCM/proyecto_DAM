    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtEmployeeName.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                    rootPane,
                    "Por favor, introduzca el nombre del trabajador."
            );
            txtEmployeeName.requestFocus();
            return;
        }
        if (txtLastNames.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                    rootPane,
                    "Por favor, introduzca los apellidos del trabajador."
            );
            txtLastNames.requestFocus();
            return;
        }
        if (txtNationalId.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                    rootPane,
                    "Por favor, introduzca el DNI del trabajador."
            );
            txtNationalId.requestFocus();
            return;
        }
        if (txtSalary.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                    rootPane,
                    "Por favor, introduzca el salario del trabajador."
            );
            txtSalary.requestFocus();
            return;
        }
        if (txtLogin.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                    rootPane,
                    "Por favor, introduzca un login del trabajador."
            );
            txtLogin.requestFocus();
            return;
        }
        if (txtPassword.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                    rootPane,
                    "Por favor, introduzca una contrase√±a para eltrabajador."
            );
            txtPassword.requestFocus();
            return;
        }

        Employee employee = new Employee();
        CrudEmployee crudEmployee = new CrudEmployee();

        employee.setName(txtEmployeeName.getText());
        employee.setLast_names(txtLastNames.getText());
        employee.setNational_id(txtNationalId.getText());
        employee.setAddress(txtAddress.getText());
        employee.setPhone(txtPhone.getText());
        employee.setEmail(txtEmail.getText());
        employee.setSalary(Double.parseDouble(txtSalary.getText()));
        int selectedItem = cbAccess.getSelectedIndex();
        employee.setAccess((String) cbAccess.getItemAt(selectedItem));
        employee.setLogin(txtLogin.getText());
        employee.setPassword(txtPassword.getText());
        selectedItem = cbStatus.getSelectedIndex();
        employee.setStatus((String) cbStatus.getItemAt(selectedItem));

        if (action.equals("guardar")) {
            if (crudEmployee.create(employee)) {
                JOptionPane.showMessageDialog(
                        rootPane,
                        "Se registr√≥ el trabajador satisfactoriamente."
                );
                findEmployees("");
                disableComponents();

            }
        } else if (action.equals("editar")) {
            employee.setId_person(Integer.parseInt(
                    txtIdPerson.getText()
            ));

            if (crudEmployee.update(employee)) {
                JOptionPane.showMessageDialog(
                        rootPane,
                        "Se actualiz√≥ el trabajador satisfactoriamente."
                );
                findEmployees("");
                disableComponents();

            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

