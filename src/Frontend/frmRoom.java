        action = "editar";

        int row = table.rowAtPoint(evt.getPoint());

        txtIdRoom.setText(table.getValueAt(row, 0).toString());
        txtRoomNumber.setText(table.getValueAt(row, 1).toString());
        cbFloor.setSelectedItem(table.getValueAt(row, 2).toString());
