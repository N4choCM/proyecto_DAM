    /**
     * Procedure that finds Products by their name field. It also shows the
     * total number of Products registered in the DB.
     *
     * @param search The product name data to be found in the DB entries.
     */
    void findEmployees(String search) {

        try {
            DefaultTableModel model;
            CrudEmployee crudEmployee = new CrudEmployee();
            model = crudEmployee.findEmployee(search);

            tableEmployees.setModel(model);
            hideColumns();
            lblTotalRegistries.setText(
                    "Total registros: "
                    + Integer.toString(crudEmployee.totalRegistries)
            );
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }

    }

