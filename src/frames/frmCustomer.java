    /**
     * Procedure that finds Products by their name field. It also shows the
     * total number of Products registered in the DB.
     * @param search The product name data to be found in the DB entries.
     */
    void findCustomers(String search) {

        try {
            DefaultTableModel model;
            CrudCustomer crudCustomer = new CrudCustomer();
            model = crudCustomer.findCustomer(search);

            tableCustomers.setModel(model);
            hideColumns();
            lblTotalRegistries.setText(
                    "Total registros: "
                    + Integer.toString(crudCustomer.totalRegistries)
            );
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }

    }
    
