    /**
     * Procedure that deletes a Product in the DB once the btnDelete is clicked.
     * Once the Product is successfully deleted, the disableComponents()
     * procedure is called.
     *
     * @param evt A click on btnDelete.
     */
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!txtIdProduct.getText().equals("")) {
            int confirmation = JOptionPane.showConfirmDialog(
                    rootPane,
                    "¿Está seguro de eliminar el producto?",
                    "Confirmar",
                    2
            );

            if (confirmation == 0) {
                CrudProduct crudProduct = new CrudProduct();
                Product product = new Product();

                product.setId_product(
                        Integer.parseInt(
                                txtIdProduct.getText()
                        ));
                crudProduct.delete(product);
                findByProductName("");
                disableComponents();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

