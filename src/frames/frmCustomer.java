    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtCustomerName.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                rootPane,
                "Por favor, introduzca el nombre del cliente."
            );
            txtCustomerName.requestFocus();
            return;
        }
        if (txtLastNames.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                rootPane,
                "Por favor, introduzca los apellidos del cliente."
            );
            txtLastNames.requestFocus();
            return;
        }
        if (txtNationalId.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                rootPane,
                "Por favor, introduzca el DNI del cliente."
            );
            txtNationalId.requestFocus();
            return;
        }
        if (txtCustomerId.getText().length() == 0) {
            JOptionPane.showConfirmDialog(
                rootPane,
                "Por favor, introduzca el código del cliente."
            );
            txtCustomerId.requestFocus();
            return;
        }

        Customer customer = new Customer();
        CrudCustomer crudCustomer = new CrudCustomer();

        customer.setName(txtCustomerName.getText());
        customer.setLast_names(txtLastNames.getText());
        customer.setNational_id(txtNationalId.getText());
        customer.setAddress(txtAddress.getText());
        customer.setPhone(txtPhone.getText());
        customer.setEmail(txtEmail.getText());
        customer.setId_customer(txtCustomerId.getText());

        if (action.equals("guardar")) {
            if (crudCustomer.create(customer)) {
                JOptionPane.showMessageDialog(
                    rootPane,
                    "Se registró el cliente satisfactoriamente."
                );
                findCustomers("");
                disableComponents();

            }
        } else if (action.equals("editar")) {
            customer.setId_person(Integer.parseInt(
                txtIdPerson.getText()
            ));

            if (crudCustomer.update(customer)) {
                JOptionPane.showMessageDialog(
                    rootPane,
                    "Se actualizó el cliente satisfactoriamente."
                );
                findCustomers("");
                disableComponents();

            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

